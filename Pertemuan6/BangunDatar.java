public class BangunDatar {
    private String jenis;

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

    public float hitungKeliling() {
        float p = 0;
        float l = 0;
        return 2 * (p + l);
    }

    public float hitungLuas() {
        float p = 0;
        float l = 0;
        return p * l;
    }
}
